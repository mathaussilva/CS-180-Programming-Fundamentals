
public class Average {

	public static void main(String[] args) {
		
		// Create and set 3 integer variables named test1, test2, and test3 to values 80, 90, and 100
		int test1 = 80, test2 = 90, test3 = 100;
		
		// Create and set double variable named avg to the sum of the three tests divided by 3
		double avg = (test1 + test2 + test3)/3;
		
		// Print functions
		System.out.println("Test 1 = " + test1);
		System.out.println("Test 2 = " + test2);
		System.out.println("Test 3 = " + test3);
		System.out.println("The average of the three tests is: " + avg);

	}

}
